package fi.vtt;

import javax.ws.rs.GET;
import javax.ws.rs.Path;
import javax.ws.rs.Produces;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

@Path("/hello")
public class HelloResource {
    // http://localhost:8080/ActivityLogger/rest/hello
    // https://<your server>:<port>/ActivityLogger/rest/hello

    @GET
    @Produces("text/html")
    public String getMessage() {
        Logger logger = LoggerFactory.getLogger(HelloResource.class);
        logger.info("getMessage()");
        StringBuffer sb = new StringBuffer(256);
        sb.append("<html>");
        sb.append("<head><title>ActivityLogger hello test</title></head>");
        sb.append("<body>");

        sb.append("Hello : " + System.currentTimeMillis());

        sb.append("</body>");
        sb.append("</html>");
        return sb.toString();
    }

}